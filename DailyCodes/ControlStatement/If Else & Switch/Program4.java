class SwitchDemo{
	public static void main(String[]args){
		String Trupti="A";

		switch(Trupti){
		
		case 1:
			System.out.println("A");
			break;
		case 2:
			System.out.println("C");
			break;
		case 3:
			System.out.println("D");
			break;
		
		default:
			System.out.println("null");
			}
	}
}

