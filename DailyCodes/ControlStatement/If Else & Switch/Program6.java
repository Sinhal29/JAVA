class SwitchDemoT{
	public static void main(String[]args){

		int x=60;
		switch(x){

			case 30>=30:
				System.out.println(20+30);
				break;
			case 40<=20:
				System.out.println(25+25);
				break;
			case 60:
				System.out.println(60);
				break;
			default:
				System.out.println("In Default");
				break;
		}

		System.out.println("End  Code");
	}
}
